document.addEventListener('mouseout', function(e) {
    if (e.target.classList.contains('tooltip')) {
        const tooltip = e.target.querySelector('.tooltip-text');
        if (tooltip) {
            tooltip.style.visibility = 'hidden';
            tooltip.style.opacity = '0';
        }
    }
});