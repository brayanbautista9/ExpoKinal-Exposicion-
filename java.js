// Funciones auxiliares
function formatText(text) {
    // Normaliza saltos de línea y divide en párrafos
    return text
        .replace(/\n+/g, '\n')  // Elimina saltos múltiples
        .split('\n')            // Divide por saltos de línea
        .filter(paragraph => paragraph.trim() !== '')  // Elimina líneas vacías
        .map(paragraph => `<p>${paragraph.trim()}</p>`) // Envuelve en <p>
        .join('');              // Une todo
}
function updateTimerDisplay() {
    const minutes = Math.floor(seconds / 60).toString().padStart(2, '0');
    const secs = (seconds % 60).toString().padStart(2, '0');
    timeDisplay.textContent = `${minutes}:${secs}`;
}

function showNotification(type, title, message) {
    notification.className = `notification ${type} show`;
    notificationIcon.className = `fas ${
        type === 'success' ? 'fa-check-circle' : 
        type === 'warning' ? 'fa-exclamation-triangle' : 'fa-info-circle'
    } notification-icon`;
    notificationMessage.textContent = message;
    
    // Ocultar automáticamente después de 5 segundos
    setTimeout(() => {
        hideNotification();
    }, 5000);
}

function hideNotification() {
    notification.classList.remove('show');
}

